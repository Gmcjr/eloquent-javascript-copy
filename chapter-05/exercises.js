function flatten(array) {
 return array.reduce(function(acc, curr) {
    return acc.concat(curr);
  }, [])
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

function loop(value, test, update, body) {
  for (let curr = value; test(curr); curr = update(curr)) {
    body(curr);
  }
}

