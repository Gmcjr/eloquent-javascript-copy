// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
  // returns sum of numbers in array
  // create sum variable starting at 0
  let sum = 0;
//for loop iterates over array  
  for (let i = 0; i < array.length; i++) {
// add each element and update sum
    sum += array[i];
  }
  return sum;
}

